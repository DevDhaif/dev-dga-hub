// gen-illustrations - inlines public/illustrations/ SVGs into a typed registry at src/lib/illustrations.generated.ts.
// Markup is rendered via dangerouslySetInnerHTML - sources must stay first-party and SVGO-sanitized.

import { readFileSync, writeFileSync, mkdirSync, readdirSync, existsSync } from 'node:fs';
import { join, dirname, basename } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, '..');
const inDir = join(root, 'public', 'illustrations');
const outPath = join(root, 'src', 'lib', 'illustrations.generated.ts');

const FLIP_ATTR = /\s*data-flip=["'][^"']*["']/i;

function readBucket(sub) {
  const dir = join(inDir, sub);
  const out = {};
  if (!existsSync(dir)) return out;
  for (const file of readdirSync(dir).sort()) {
    if (!file.endsWith('.svg')) continue;
    const key = basename(file, '.svg');
    let svg = readFileSync(join(dir, file), 'utf8').trim();
    const flip = /data-flip=["']?true["']?/i.test(svg);
    svg = svg.replace(FLIP_ATTR, '');
    out[key] = { svg, flip };
  }
  return out;
}

function serialize(name, map) {
  const entries = Object.entries(map)
    .map(([k, v]) => {
      const flip = v.flip ? ', flip: true' : '';
      return `  ${JSON.stringify(k)}: { svg: ${JSON.stringify(v.svg)}${flip} },`;
    })
    .join('\n');
  return `export const ${name}: Record<string, IllustrationEntry> = {\n${entries}\n};`;
}

function main() {
  const components = readBucket('components');
  const categories = readBucket('categories');
  const sections = readBucket('sections');

  const header =
    '// AUTO-GENERATED by scripts/gen-illustrations.mjs - do not edit.\n' +
    '// Rebuilt from public/illustrations/ (itself built from assets/illustrations/).\n\n' +
    'export interface IllustrationEntry {\n' +
    '  /** Inline <svg> markup - two-color (currentColor + var(--ddga-color-primary)). */\n' +
    '  svg: string;\n' +
    '  /** Direction-specific art: wrap in .rtl-flip-x under RTL. */\n' +
    '  flip?: boolean;\n' +
    '}\n\n';

  const body = [
    serialize('componentIllustrations', components),
    serialize('categoryIllustrations', categories),
    serialize('sectionIllustrations', sections),
  ].join('\n\n');

  mkdirSync(dirname(outPath), { recursive: true });
  writeFileSync(outPath, header + body + '\n');
  console.log(
    `[illustrations] registry → src/lib/illustrations.generated.ts ` +
      `(${Object.keys(components).length} component, ${Object.keys(categories).length} category, ` +
      `${Object.keys(sections).length} section)`,
  );
}

main();
